export function descendingComparator(a, b, orderBy) {
  if (b[orderBy] < a[orderBy]) {
    return -1;
  }
  if (b[orderBy] > a[orderBy]) {
    return 1;
  }
  return 0;
}

export function getComparator(order, orderBy) {
  return order === 'desc'
    ? (a, b) => descendingComparator(a, b, orderBy)
    : (a, b) => -descendingComparator(a, b, orderBy);
}

export function stableSort(array, comparator) {
  const stabilizedThis = array && array.length > 0 && array?.map((el, index) => [el, index]);
  console.log('Stabilize sort: ', stabilizedThis);
  stabilizedThis &&
    stabilizedThis.length > 0 &&
    stabilizedThis?.sort((a, b) => {
      const order = comparator(a[0], b[0]);
      if (order !== 0) {
        return order;
      }
      return a[1] - b[1];
    });
  return stabilizedThis && stabilizedThis.length > 0 && stabilizedThis?.map((el) => el[0]);
}
